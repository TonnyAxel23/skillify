    <script>
        // Password strength indicator
        document.getElementById('password').addEventListener('input', function() {
            const password = this.value;
            const strengthDiv = document.getElementById('passwordStrength');
            
            if (password.length < 6) {
                strengthDiv.innerHTML = 'Password too short';
                strengthDiv.className = 'password-strength weak';
            } else {
                let strength = 'weak';
                if (password.match(/[a-z]/) && password.match(/[A-Z]/) && password.match(/[0-9]/) && password.length >= 8) {
                    strength = 'strong';
                } else if (password.length >= 6) {
                    strength = 'medium';
                }
                
                strengthDiv.innerHTML = 'Password strength: ' + strength;
                strengthDiv.className = 'password-strength ' + strength;
            }
        });
        
        // Password match validation
        document.getElementById('confirm_password').addEventListener('input', function() {
            const password = document.getElementById('password').value;
            const confirm = this.value;
            
            if (password !== confirm) {
                this.style.borderColor = '#f00';
            } else {
                this.style.borderColor = '#0f0';
            }
        });
